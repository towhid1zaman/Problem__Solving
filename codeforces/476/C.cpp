const int mod = 1000000007;

ll mul(ll a, ll b){
    return (a*b)%mod;
}
void task(){
    ll a, b; cin >> a >> b;
    ll ans = 0;
    for(ll k = 1; k<=a; k++){
        ans+= mul((k*b+1)%mod , ((b*(b-1))/2)%mod);
        ans%=mod;
    }

    cout << ans << endl;
}

