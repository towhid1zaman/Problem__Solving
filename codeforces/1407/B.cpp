ll gcd(ll a,ll b){
    return b ? gcd(b,a%b) : a;
}
ll lcm(ll a, ll b){
    return a/ gcd(a,b)*b;
}

void task(){
	int n; cin >> n;
	std::vector<int> a(n);
	for(int i =0; i<n; i++){
		cin >> a[i];
	}
	std::vector<int> c(1,0), b;
	std::vector<bool> used(n,false);

	for(int i = 0; i<n; i++){
		int best = -1, id = -1;
		for(int j = 0; j<n; j++){
			if(!used[j] and gcd(c.back(), a[j]) > best){
				best = gcd(c.back(), a[j]);
				id = j;
			}
		}
		used[id] = true;
		c.push_back(best);
		b.push_back(a[id]);
	}
	for(auto v: b)cout << v <<' '; cout <<endl;
}


