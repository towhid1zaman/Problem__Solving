void task(){
    ll l, r , m; cin >> l >> r >> m;
    for(ll a = l; a<=r; a++){
        ll rem = m%a;
        ll rem2 = a - rem;
        if(rem ==  0){
            cout << a <<' '<<a<<' '<<a<<endl;
            return;
        }
        else if(min(rem, rem2) <= (r-l)){
            if(rem2<=(r-l)){
                cout << a << ' '<<l<<' '<<l+rem2<<' ';
                return;
            }
            else{
                cout << a <<' '<<r <<' '<<r-rem <<endl;
                return;
            }
        }
    }
    cout << endl;
}

